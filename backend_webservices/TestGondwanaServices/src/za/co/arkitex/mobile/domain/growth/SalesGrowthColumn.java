package za.co.arkitex.mobile.domain.growth;

public class SalesGrowthColumn {

	private String mtdMonth;
	private String mtdPreviousMonth;
	private String ytd;
	private String ytdPreviousYear;
	
	public String getMtdMonth() {
		return mtdMonth;
	}
	public void setMtdMonth(String mtdMonth) {
		this.mtdMonth = mtdMonth;
	}
	public String getMtdPreviousMonth() {
		return mtdPreviousMonth;
	}
	public void setMtdPreviousMonth(String mtdPreviousMonth) {
		this.mtdPreviousMonth = mtdPreviousMonth;
	}
	public String getYtd() {
		return ytd;
	}
	public void setYtd(String ytd) {
		this.ytd = ytd;
	}
	public String getYtdPreviousYear() {
		return ytdPreviousYear;
	}
	public void setYtdPreviousYear(String ytdPreviousYear) {
		this.ytdPreviousYear = ytdPreviousYear;
	}
	

	
	
}
